package note;

public class Switch {
public static void main(String[] args) {
	String colour="red";
	switch (colour) {
	case "red":
		System.out.println("the colour is red");
	
	case "black":
			System.out.println("the colour is black");
			break;
	default:
		System.out.println(" the colour is white");
		break;
	}
	}
}
